#!/usr/bin/env node
// Generate public/llms-full.txt by concatenating every doc page in
// src/content/docs/ in sidebar order. Run via `npm run build:llms`
// before `astro build` (or after editing docs).

import { readFile, writeFile, stat } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, '..');
const docsRoot = join(repoRoot, 'src/content/docs');
const outFile = join(repoRoot, 'public/llms-full.txt');

const SITE = 'https://kinder.patrykgolabek.dev';

// Sidebar order matches astro.config.mjs so the generated file scans the
// way the website's left nav reads. Page slugs without ".md/.mdx" extension.
const order = [
  'index',
  'installation',
  'quick-start',
  'configuration',
  'addons/metallb',
  'addons/envoy-gateway',
  'addons/metrics-server',
  'addons/coredns',
  'addons/headlamp',
  'addons/local-registry',
  'addons/cert-manager',
  'addons/local-path-provisioner',
  'addons/nvidia-gpu',
  'guides/tls-web-app',
  'guides/hpa-auto-scaling',
  'guides/local-dev-workflow',
  'guides/dynamic-storage',
  'guides/host-directory-mounting',
  'guides/multi-version-clusters',
  'guides/k8s-1-36-whats-new',
  'guides/working-offline',
  'cli-reference/profile-comparison',
  'cli-reference/json-output',
  'cli-reference/troubleshooting',
  'cli-reference/load-images',
  'cli-reference/cluster-lifecycle',
  'cli-reference/snapshot',
  'cli-reference/dev',
  'known-issues',
  'changelog',
];

async function findFile(slug) {
  for (const ext of ['.md', '.mdx']) {
    const p = join(docsRoot, slug + ext);
    try {
      await stat(p);
      return p;
    } catch {}
  }
  return null;
}

function stripFrontmatter(text) {
  if (!text.startsWith('---\n')) return { meta: {}, body: text };
  const end = text.indexOf('\n---', 4);
  if (end === -1) return { meta: {}, body: text };
  const block = text.slice(4, end);
  const body = text.slice(end + 4).replace(/^\n+/, '');
  const meta = {};
  for (const line of block.split('\n')) {
    const m = line.match(/^([a-z_][a-z0-9_]*):\s*(.*?)\s*$/i);
    if (m) {
      let v = m[2];
      if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) {
        v = v.slice(1, -1);
      }
      meta[m[1]] = v;
    }
  }
  return { meta, body };
}

function stripImports(text) {
  // Drop top-level mdx imports/JSX; LLMs don't need them.
  return text
    .split('\n')
    .filter((line) => !/^import\s+.+from\s+['"]/.test(line))
    .join('\n');
}

const sections = [];
const missing = [];

const intro = `# kinder — Full Documentation

> kind, but with everything you actually need.

kinder is a fork of kind (Kubernetes IN Docker) that ships a batteries-included local Kubernetes development experience. This file is the full text of the kinder documentation, concatenated for LLM ingestion.

Author: Patryk Golabek (https://patrykgolabek.dev)
Source: https://github.com/PatrykQuantumNomad/kinder
Documentation: ${SITE}
Generated by: scripts/build-llms-full.mjs

---
`;
sections.push(intro);

for (const slug of order) {
  const file = await findFile(slug);
  if (!file) {
    missing.push(slug);
    continue;
  }
  const raw = await readFile(file, 'utf8');
  const { meta, body } = stripFrontmatter(raw);
  const cleanBody = stripImports(body).trim();
  const url = slug === 'index' ? SITE + '/' : `${SITE}/${slug}/`;
  const title = meta.title || slug;
  sections.push(`\n## ${title}\n\nURL: ${url}\n${meta.description ? `\n> ${meta.description}\n` : ''}\n${cleanBody}\n\n---\n`);
}

if (missing.length) {
  console.error(`[build-llms-full] missing files for slugs: ${missing.join(', ')}`);
  process.exit(1);
}

await writeFile(outFile, sections.join(''), 'utf8');
const finalText = sections.join('');
console.log(`[build-llms-full] wrote ${outFile}`);
console.log(`[build-llms-full] ${order.length} pages, ${finalText.split('\n').length} lines, ${(finalText.length / 1024).toFixed(1)} KiB`);
